def turn_left(position_x):
    if 1200 > position_x > 0:
        position_x += 1
    return position_x
def turn_right(position_x):
    if 1200 > position_x> 0:
        position_x-= 1
    return position_x


def turn_up(position_y):
    if 1200 > position_y > 0:
        position_y += 1
    return position_y


def turn_down(position_y):
    if 1200 > position_y < 0:
        position_y -= 1
    return position_y



